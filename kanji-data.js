const KANJI_LESSONS = [];

function addKanjiLesson(week, day, title, titleTh, scene, rows) {
  const items = rows.map((row, index) => {
    const [kanji, word, reading, meaning] = row.split("|");
    return { id: `k-${week}-${day}-${index + 1}`, kanji, word, reading, meaning, week, day };
  });
  KANJI_LESSONS.push({ id: `w${week}d${day}`, week, day, title, titleTh, scene, items });
}

